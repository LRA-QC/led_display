#include <stdio.h>
#include <string.h>
unsigned char FONT[][8] = { 0x3e,0x67,0x6f,0x7b,0x73,0x63,0x3e,0x0,0x0c,0x1c,0x0c,0x0c,0x0c,0x0c,0x3f, 0x0, 0x1E, 0x33, 0x06, 0x1C, 0x30, 0x33, 0x3F, 0x00, 0x1E, 0x33, 0x03, 0x1C, 0x03, 0x33, 0x1E, 0x00, 7, 15, 27, 51, 63, 3, 3, 0x00, 0x3F, 48, 0x3e, 3, 3, 0x33, 0x1E, 0x00,0xe, 24, 48, 0x3e, 0x33, 0x33, 0x1E, 0x00,0x3F, 0x33, 0x33, 3, 3,3,3, 0x00,0x1E, 0x33, 0x33, 0x1E, 0x33, 0x33, 0x1E, 0x00,0x1E, 0x33, 0x33, 31, 3, 3, 3, 0x0};
void draw(const char *text) { 
  const unsigned char *p=(const unsigned char *)text;
  for (int j=0;j<8;++j) { //font is 8 pixels tall
    for (int k=0;k<strlen(text);++k) { //draw X quantity of symbol 
      if (p[k]>='0' && p[k]<='9') {
        for (int l=0;l<8;++l) { //draw 8 pixels wide
          if ( FONT[p[k]-'0'][j] & ( 1 << (7-l)) )
            printf("#");
          else 
            printf(" ");
        }
      }
    }
    printf("\n");
  }
}
void main(void){  //compile with the following:   gcc -o led main.c 
  draw("0123456789");
}